public class CustomerFavor extends BasePo{

	private String customerId;
	
	private String favorObject;
	
	private String favorStatus;
	
	private String favorType;

	public String getCustomerId() {
		return customerId;
	}

	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}

	public String getFavorObject() {
		return favorObject;
	}

	public void setFavorObject(String favorObject) {
		this.favorObject = favorObject;
	}

	public String getFavorStatus() {
		return favorStatus;
	}

	public void setFavorStatus(String favorStatus) {
		this.favorStatus = favorStatus;
	}

	public String getFavorType() {
		return favorType;
	}

	public void setFavorType(String favorType) {
		this.favorType = favorType;
	}

	
}
