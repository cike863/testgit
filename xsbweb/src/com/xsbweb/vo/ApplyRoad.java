package com.xsbweb.vo;

public class ApplyRoad {

	private String company;
	
	private String step;
	
	private String isVenture;
	
	private String financingType;
	
	private String turnoverScale;
	
	private String name;
	private String position;
	private String phoneNo;
	private String wx;
	private String email;

	private String customerId;
	
	public String getCustomerId() {
		return customerId;
	}

	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPosition() {
		return position;
	}

	public void setPosition(String position) {
		this.position = position;
	}

	public String getPhoneNo() {
		return phoneNo;
	}

	public void setPhoneNo(String phoneNo) {
		this.phoneNo = phoneNo;
	}

	public String getWx() {
		return wx;
	}

	public void setWx(String wx) {
		this.wx = wx;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}

	public String getStep() {
		return step;
	}

	public void setStep(String step) {
		this.step = step;
	}

	public String getIsVenture() {
		return isVenture;
	}

	public void setIsVenture(String isVenture) {
		this.isVenture = isVenture;
	}

	public String getFinancingType() {
		return financingType;
	}

	public void setFinancingType(String financingType) {
		this.financingType = financingType;
	}

	public String getTurnoverScale() {
		return turnoverScale;
	}

	public void setTurnoverScale(String turnoverScale) {
		this.turnoverScale = turnoverScale;
	}

}
